<p>Childhood is the time to instil lifelong eating and exercise habits that contribute to good health. Nutrition affects children’s ability to learn, develop and stay healthy throughout life. Getting children to develop healthy eating habits is about as easy as herding cats. It’s been often asked why is it that everything that is good for you tastes bad, yet everything that is bad for you tastes good. Even if you have the answer, try explaining it to a 6 year old. </p>
      <p>Feeding your child a nutritious meal is a challenge to most parents. Sometimes, parents would be successful in feeding their child the right quantity of food. Infants and pre-school children are extremely vulnerable and succumb when the diet is poor in quality and quantity.</p>
      <p>Family, environment, social trends, media, peer pressure and illness or disease affect the food habits of your child. Parents are equally responsible for children's eating habits and table manners. By teaching your child healthy eating habits, and modelling these behaviours in yourself, you can help your child maintain a healthy weight and normal growth. Also, the eating habits your child pick up when they are young will help them maintain a healthy lifestyle when they are adults.</p>
    
