    <h3>Some approaches that parents can take to develop healthy eating habits in their children are -</h3>
    
    <ul style ="list-style:disc;display: list-item"><li style ="list-style:disc;display: list-item">
            Do not let your child watch TV during meals. Talk about something interesting that will increase your child's general knowledge or you may even talk about their day at school. Watching television with multiple media cues to eat is considered as a factor in excessive weight gain for children especially in age group of six to seven years.
        </li>
        <li style ="list-style:disc;display: list-item">
            Children like to try out different food. Parents should offer a variety of foods including their child’s favourites. They can also try substitutions within the same food group. 
        </li>
        <li style ="list-style:disc;display: list-item">
           Pre-school children do best with small servings of food offered several times a day because of their smaller capacity and variable appetites.
        </li>
        <li style ="list-style:disc;display: list-item">
           Most children eat four to six times a day, which means snacks should be chosen carefully so that it is dense in nutrients and not limited to biscuits, chips, crisps, colas, chocolates and pastries.
        </li>
        <li style ="list-style:disc;display: list-item">
           Parents can include wholesome snacks like fresh fruits, cheese, raw vegetable sticks, milk, fruit juices, whole grain and gram products and sandwiches.
        </li>
        <li style ="list-style:disc;display: list-item">
           Avoid giving any food or drink within 1½ hours of a meal to your child. It does not take much to satisfy a child's appetite but even small snacks may result in poor eating at meal time.
        </li>
    </ul>
