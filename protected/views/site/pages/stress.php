<p>High stress levels and weight gain often go hand in hand. Stress can also be an obstacle while losing weight.</p>
      <p>When faced with a stressful situation, our brains signal an acute immediate response causing the adrenal glands to release hormone called cortisol and release of a chemical in the brain called Neuropeptide Y, along with Norepinephrine.</p>
      <p>Cortisol is a critical hormone with many actions in the body. Cortisol is secreted by the adrenal glands in a pattern called a diurnal variation, which means that levels of cortisol in the bloodstream vary depending upon the time of day (normally, cortisol levels are highest in the early morning and lowest around midnight). Cortisol is important for the maintenance of blood pressure as well as the provision of energy for the body. Cortisol stimulates fat and carbohydrate metabolism for fast energy, and stimulates insulin release and maintenance of blood sugar levels. The end result of these actions can be an increase in appetite. When cortisol levels increase in response to stress, blood sugar levels rise as proteins are converted to glucose to serve as additional fuel. The unused glucose ends up being stored as fat once the stress is over, resulting in weight gain. Exposure to cortisol over long term can lead to weight gain, as your appetite and insulin levels are continuously increased. Neuropeptide Y is a powerful appetite-stimulating chemical which especially increases the desire for carbohydrate-rich foods. </p>
      <p>Often, the strongest longings for food occur when you are at the weak point emotionally. You may reach for food when you are frustrated, angry or sad or simply looking for ways to occupy your mind. Emotional eating provides temporarily relief to stress along with excess calories and weight gain. Although you can’t avoid stress completely, you can learn to deal with it more effectively. </p><p>&nbsp;</p>
    
